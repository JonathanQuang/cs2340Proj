package cs2340.spacetraders.entity;

/**
 * Class that stores the total number of encounters in the game
 */
public class DataStorage {
    private int totalEncounters;

    /**
     * Getter for total encounters
     * @return total encounters
     */
    public int getTotalEncounters() {
        return totalEncounters;
    }

    /**
     * Setter for total encounters
     * @param totalEncounters total encounters
     */
    public void setTotalEncounters(int totalEncounters) {
        this.totalEncounters = totalEncounters;
    }
}
